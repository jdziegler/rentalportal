"use client";

import Link from "next/link";
import { usePathname, useRouter } from "next/navigation";
import { useState } from "react";
import type { TenantSession } from "@/lib/tenant-auth";

const NAV_ITEMS = [
  { href: "/tenant/dashboard", label: "Pay Rent", icon: CreditCardIcon },
  { href: "/tenant/documents", label: "Documents", icon: DocumentIcon },
  { href: "/tenant/maintenance", label: "Maintenance", icon: WrenchIcon },
  { href: "/tenant/messages", label: "Messages", icon: ChatIcon },
];

export default function TenantPortalShell({
  session,
  children,
}: {
  session: TenantSession;
  children: React.ReactNode;
}) {
  const pathname = usePathname();
  const router = useRouter();
  const [loggingOut, setLoggingOut] = useState(false);

  async function handleLogout() {
    setLoggingOut(true);
    await fetch("/api/tenant/logout", { method: "POST" });
    router.push("/tenant");
  }

  return (
    <div className="min-h-screen bg-gray-50">
      <header className="bg-white border-b border-gray-200">
        <div className="max-w-4xl mx-auto px-4 py-3 flex items-center justify-between">
          <Link href="/tenant/dashboard" className="text-lg font-bold text-gray-900">
            PropertyPilot
          </Link>
          <div className="flex items-center gap-3">
            <span className="text-sm text-gray-500 hidden sm:inline">
              {session.identifier}
            </span>
            <button
              onClick={handleLogout}
              disabled={loggingOut}
              className="text-sm text-gray-500 hover:text-gray-700 transition"
            >
              {loggingOut ? "..." : "Sign out"}
            </button>
          </div>
        </div>
      </header>

      <nav className="bg-white border-b border-gray-200">
        <div className="max-w-4xl mx-auto px-4 flex gap-1">
          {NAV_ITEMS.map((item) => {
            const active = pathname === item.href;
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex items-center gap-1.5 px-3 py-2.5 text-sm font-medium border-b-2 transition ${
                  active
                    ? "border-indigo-600 text-indigo-600"
                    : "border-transparent text-gray-500 hover:text-gray-700"
                }`}
              >
                <item.icon className="w-4 h-4" />
                {item.label}
              </Link>
            );
          })}
        </div>
      </nav>

      <main className="max-w-4xl mx-auto px-4 py-6">{children}</main>
    </div>
  );
}

